class StringDemo3{
	public static void main(String args[]){
		StringBuffer sb = new StringBuffer("Hello");
		System.out.println(sb);
		sb.append("Duniya");
		System.out.println(sb);
		
		StringBuilder sb1 = new StringBuilder("Hello");
		System.out.println(sb1);
		sb1.append("CDAC");
		System.out.println(sb1);
		
		
	}
}