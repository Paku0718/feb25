public class BooleanLiterals{
	public static void main(String args[]){
		boolean day = true;
		boolean two = false;
		
		if(day){
			System.out.println("have a good day");
		} else {
			System.out.println("have a bad day");
		}
		
		if(two){
			System.out.println("great to see you");
		} else {
			System.out.println("hello");
		}
		
	}
}